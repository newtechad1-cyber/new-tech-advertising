// ─────────────────────────────────────────────────

function KnowledgeHealth({ allAssets, relationships }) {
  const totalAssets = allAssets.length;
  const approvedRels = relationships.filter(r => r.status === 'approved').length;
  const pendingRels = relationships.filter(r => r.status === 'suggested' || r.status === 'pending_review').length;
  const connectedAssets = new Set();
  relationships.filter(r => r.status === 'approved').forEach(r => {
    connectedAssets.add(r.source_id);
    connectedAssets.add(r.target_id);
  });
  const connectionRate = totalAssets > 0 ? Math.round((connectedAssets.size / totalAssets) * 100) : 0;

  const domainCounts = {};
  KNOWLEDGE_DOMAINS.forEach(d => { domainCounts[d.key] = 0; });
  allAssets.forEach(a => {
    if (domainCounts[a.domain] !== undefined) domainCounts[a.domain]++;
  });

  return (
    <div className="space-y-3">
      {/* Stats Row */}
      <div className="grid grid-cols-4 gap-2">
        <div className="bg-slate-800/40 border border-slate-700/50 rounded-xl p-3 text-center">
          <p className="text-xl font-black text-blue-400">{totalAssets}</p>
          <p className="text-[9px] font-mono text-slate-500 uppercase">Assets</p>
        </div>
        <div className="bg-slate-800/40 border border-slate-700/50 rounded-xl p-3 text-center">
          <p className="text-xl font-black text-emerald-400">{approvedRels}</p>
          <p className="text-[9px] font-mono text-slate-500 uppercase">Connected</p>
        </div>
        <div className="bg-slate-800/40 border border-slate-700/50 rounded-xl p-3 text-center">
          <p className="text-xl font-black text-amber-400">{pendingRels}</p>
          <p className="text-[9px] font-mono text-slate-500 uppercase">Pending</p>
        </div>
        <div className="bg-slate-800/40 border border-slate-700/50 rounded-xl p-3 text-center">
          <p className="text-xl font-black text-cyan-400">{connectionRate}%</p>
          <p className="text-[9px] font-mono text-slate-500 uppercase">Health</p>
        </div>
      </div>

      {/* Domain breakdown bar */}
      <div className="bg-slate-800/30 border border-slate-700/30 rounded-xl p-3">
        <p className="text-[10px] font-mono text-slate-500 uppercase mb-2">Domain Distribution</p>
        <div className="flex h-3 rounded-full overflow-hidden gap-px">
          {KNOWLEDGE_DOMAINS.map(d => {
            const count = domainCounts[d.key] || 0;
            const pct = totalAssets > 0 ? (count / totalAssets) * 100 : 0;
            if (pct === 0) return null;
            return (
              <div
                key={d.key}
                className={`${d.bg} border ${d.border} transition-all`}
                style={{ width: `${pct}%` }}
                title={`${d.label}: ${count} assets (${Math.round(pct)}%)`}
              />
            );
          })}
        </div>
        <div className="grid grid-cols-2 gap-1 mt-2">
          {KNOWLEDGE_DOMAINS.map(d => (
            <div key={d.key} className="flex items-center gap-1.5">
              <div className={`w-2 h-2 rounded-sm ${d.bg} border ${d.border}`} />
              <span className="text-[10px] text-slate-500">{d.label}</span>
              <span className={`text-[10px] font-mono ${d.color} ml-auto`}>{domainCounts[d.key] || 0}</span>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}

// ─────────────────────────────────────────────────
// Main Component
// ─────────────────────────────────────────────────

export default function KnowledgeNavigator() {
  // State
  const [allAssets, setAllAssets] = useState(STATIC_ASSETS);
  const [relationships, setRelationships] = useState([]);
  const [selectedAsset, setSelectedAsset] = useState(null);
  const [expandedDomains, setExpandedDomains] = useState({});
  const [isLoadingEntities, setIsLoadingEntities] = useState(true);
  const [isLoadingAI, setIsLoadingAI] = useState(false);
  const [view, setView] = useState('navigator'); // navigator | health

  // Load entity-based assets + relationships
  useEffect(() => {
    async function loadAssets() {
      setIsLoadingEntities(true);
      try {
        const entityTypes = [
          { entity: 'BlogPost', type: 'BlogPost', titleField: 'title', domain: 'thought_leadership' },
          { entity: 'KnowledgeArticle', type: 'KnowledgeArticle', titleField: 'article_title', domain: 'education_training' },
          { entity: 'CaseStudy', type: 'CaseStudy', titleField: 'title', domain: 'sales_intelligence' },
          { entity: 'SalesKnowledgeBase', type: 'SalesKnowledgeBase', titleField: 'title', domain: 'sales_intelligence' },
          { entity: 'SOPWorkflow', type: 'SOPWorkflow', titleField: 'name', domain: 'operational_knowledge' },
          { entity: 'ChatbotKnowledge', type: 'ChatbotKnowledge', titleField: 'question', domain: 'market_intelligence' },
          { entity: 'KnowledgeGapSignal', type: 'KnowledgeGapSignal', titleField: 'gap_title', domain: 'operational_knowledge' },
          { entity: 'TopicCluster', type: 'TopicCluster', titleField: 'topic', domain: 'market_intelligence' },
          { entity: 'AIPromptTemplates', type: 'AIPromptTemplates', titleField: 'template_name', domain: 'brand_creative' },
        ];

        const dynamicAssets = [];
        for (const { entity, type, titleField, domain } of entityTypes) {
          try {
            const EntityClass = base44.entities[entity];
            if (!EntityClass) continue;
            const records = await EntityClass.list();
            if (Array.isArray(records)) {
              records.forEach(r => {
                dynamicAssets.push({
                  type,
                  id: r.id,
                  title: r[titleField] || r.title || r.name || `${type} #${r.id}`,
                  domain,
                  tags: r.tags || r.category || '',
                });
              });
            }
          } catch (e) {
            console.warn(`Failed to load ${entity}:`, e);
          }
        }

        // Load relationships
        let rels = [];
        try {
          const KnowledgeRelationship = base44.entities.KnowledgeRelationship;
          if (KnowledgeRelationship) {
            rels = await KnowledgeRelationship.list();
          }
        } catch (e) {
          console.warn('KnowledgeRelationship entity not yet deployed:', e);
        }

        // Merge static + dynamic, dedup by type+id
        const seen = new Set();
        const merged = [];
        [...dynamicAssets, ...STATIC_ASSETS].forEach(a => {
          const key = `${a.type}-${a.id}`;
          if (!seen.has(key)) {
            seen.add(key);
            merged.push(a);
          }
        });

        setAllAssets(merged);
        setRelationships(Array.isArray(rels) ? rels : []);
      } catch (err) {
        console.error('Failed to load knowledge assets:', err);
      }
      setIsLoadingEntities(false);
    }
    loadAssets();
  }, []);

  // Toggle domain expansion
  const toggleDomain = useCallback((domainKey) => {
    setExpandedDomains(prev => ({ ...prev, [domainKey]: !prev[domainKey] }));
  }, []);

  // Select asset
  const selectAsset = useCallback((asset) => {
    setSelectedAsset(asset);
  }, []);

  // AI Suggestions
  const requestAISuggestions = useCallback(async (asset) => {
    setIsLoadingAI(true);
    try {
      const result = await base44.functions.invoke('buildKnowledgeRelationships', {
        asset_type: asset.type,
        asset_id: asset.id,
        asset_title: asset.title,
        asset_domain: asset.domain,
        existing_assets: allAssets.slice(0, 100).map(a => ({
          type: a.type, id: a.id, title: a.title,
          domain: a.domain, tags: a.tags
        }))
      });

      if (result?.suggestions?.length > 0) {
        const newRels = result.suggestions.map(s => ({
          id: `ai-${Date.now()}-${Math.random().toString(36).substr(2, 5)}`,
          source_type: asset.type,
          source_id: asset.id,
          source_label: asset.title,
          target_type: s.target_type,
          target_id: s.target_id,
          target_label: s.target_label,
          relationship_type: s.relationship_type,
          confidence: s.confidence,
          description: s.reasoning,
          knowledge_domain: asset.domain,
          created_by: 'ai_builder',
          ai_generated: true,
          approved: false,
          status: 'suggested',
          created_date: new Date().toISOString(),
        }));
        setRelationships(prev => [...prev, ...newRels]);
      }
    } catch (err) {
      console.error('AI suggestion error:', err);
    }
    setIsLoadingAI(false);
  }, [allAssets]);

  // Approve / Reject relationship
  const approveRelationship = useCallback(async (rel) => {
    // Save to entity
    try {
      const KnowledgeRelationship = base44.entities.KnowledgeRelationship;
      if (KnowledgeRelationship) {
        await KnowledgeRelationship.create({
          ...rel,
          status: 'approved',
          approved: true,
          last_verified: new Date().toISOString(),
        });
      }
    } catch (e) { console.warn('Could not persist relationship:', e); }

    setRelationships(prev => prev.map(r =>
      r.id === rel.id ? { ...r, status: 'approved', approved: true } : r
    ));
  }, []);

  const rejectRelationship = useCallback((rel) => {
    setRelationships(prev => prev.map(r =>
      r.id === rel.id ? { ...r, status: 'rejected', approved: false } : r
    ));
  }, []);

  return (
    <div className="bg-slate-950 min-h-screen text-slate-200 font-sans">
      {/* Header */}
      <div className="bg-slate-900 border-b border-slate-800 sticky top-0 z-40">
        <div className="max-w-[1600px] mx-auto px-4 py-3 flex items-center justify-between">
          <div className="flex items-center gap-3">
            <div className="w-10 h-10 rounded-xl bg-blue-600/20 flex items-center justify-center border border-blue-500/30">
              <Network className="w-5 h-5 text-blue-400" />
            </div>
            <div>
              <h1 className="font-bold text-white text-lg leading-tight">Knowledge Navigator™</h1>
              <p className="text-[10px] text-slate-500 font-mono uppercase tracking-wider">K-002 · Connected Knowledge System · R0.3</p>
            </div>
          </div>
          <div className="flex items-center gap-2">
            <button
              onClick={() => setView('navigator')}
              className={`text-xs px-3 py-1.5 rounded-lg transition-colors ${view === 'navigator' ? 'bg-blue-500/10 text-blue-400 border border-blue-500/20' : 'text-slate-400 hover:text-slate-200'}`}
            >
              <Network className="w-3.5 h-3.5 inline mr-1" /> Navigator
            </button>
            <button
              onClick={() => setView('health')}
              className={`text-xs px-3 py-1.5 rounded-lg transition-colors ${view === 'health' ? 'bg-emerald-500/10 text-emerald-400 border border-emerald-500/20' : 'text-slate-400 hover:text-slate-200'}`}
            >
              <BarChart3 className="w-3.5 h-3.5 inline mr-1" /> Health
            </button>
          </div>
        </div>
      </div>

      {/* Search */}
      <div className="max-w-[1600px] mx-auto px-4 py-3">
        <KnowledgeSearch
          allAssets={allAssets}
          onSelectAsset={selectAsset}
          relationships={relationships}
        />
      </div>

      {view === 'health' ? (
        <div className="max-w-3xl mx-auto px-4 py-4">
          <KnowledgeHealth allAssets={allAssets} relationships={relationships} />
        </div>
      ) : (
        /* Three-panel layout */
        <div className="max-w-[1600px] mx-auto px-4 pb-8">
          <div className="flex gap-4" style={{ minHeight: 'calc(100vh - 140px)' }}>
            {/* LEFT — Domain Navigator */}
            <div className="w-72 flex-shrink-0">
              <div className="bg-slate-900/50 border border-slate-800 rounded-2xl p-3 sticky top-20">
                <div className="flex items-center justify-between mb-3">
                  <h3 className="text-xs font-semibold text-slate-400 uppercase tracking-wider">Knowledge Domains</h3>
                  {isLoadingEntities && <Loader2 className="w-3 h-3 text-slate-500 animate-spin" />}
                </div>
                <div className="space-y-0.5">
                  {KNOWLEDGE_DOMAINS.map(domain => (
                    <DomainItem
                      key={domain.key}
                      domain={domain}
                      assets={allAssets}
                      isExpanded={expandedDomains[domain.key] || false}
                      onToggle={() => toggleDomain(domain.key)}
                      onSelectAsset={selectAsset}
                      selectedAssetId={selectedAsset?.id}
                    />
                  ))}
                </div>
              </div>
            </div>

            {/* CENTER — Asset Detail */}
            <div className="flex-1 min-w-0">
              <AssetDetail
                asset={selectedAsset}
                relationships={relationships}
                allAssets={allAssets}
                onRequestAISuggestions={requestAISuggestions}
                isLoadingAI={isLoadingAI}
              />
            </div>

            {/* RIGHT — Relationships + Recommendations */}
            <div className="w-80 flex-shrink-0 space-y-3">
              <div className="bg-slate-900/50 border border-slate-800 rounded-2xl p-3 sticky top-20 max-h-[calc(100vh-100px)] overflow-y-auto space-y-3">
                <RelationshipPanel
                  asset={selectedAsset}
                  relationships={relationships}
                  onApprove={approveRelationship}
                  onReject={rejectRelationship}
                />

                {/* I-001 Recommendation Panel */}
                {selectedAsset && (
                  <RecommendationPanel
                    contextType="knowledge"
                    contextDescription={`Viewing knowledge asset: "${selectedAsset.title || selectedAsset.name || 'Untitled'}". Domain: ${selectedAsset.domain || 'unknown'}. Looking for related knowledge and connections.`}
                    currentAssetType={selectedAsset.type || ''}
                    currentAssetId={selectedAsset.id || ''}
                    maxResults={5}
                    compact={true}
                  />
                )}
              </div>
            </div>
          </div>
        </div>
      )}
    </div>
  );
}
