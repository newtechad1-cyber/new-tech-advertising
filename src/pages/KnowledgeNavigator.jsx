function RelationshipPanel({ asset, relationships, onApprove, onReject }) {
  const [filter, setFilter] = useState('all'); // all, approved, pending

  if (!asset) {
    return (
      <div className="h-full flex items-center justify-center">
        <div className="text-center px-4">
          <GitBranch className="w-10 h-10 text-slate-700 mx-auto mb-2" />
          <p className="text-xs text-slate-500">Relationships appear here</p>
        </div>
      </div>
    );
  }

  const assetRels = relationships.filter(r =>
    r.source_id === asset.id || r.target_id === asset.id
  );

  const filtered = filter === 'all' ? assetRels :
    filter === 'approved' ? assetRels.filter(r => r.status === 'approved') :
    assetRels.filter(r => r.status === 'suggested' || r.status === 'pending_review');

  // Group by relationship type
  const grouped = {};
  filtered.forEach(r => {
    const key = r.relationship_type;
    if (!grouped[key]) grouped[key] = [];
    grouped[key].push(r);
  });

  return (
    <div className="space-y-3">
      <div className="flex items-center justify-between">
        <h3 className="text-sm font-semibold text-slate-300">Connections</h3>
        <span className="text-[10px] font-mono text-slate-500">{assetRels.length} total</span>
      </div>

      {/* Filter tabs */}
      <div className="flex gap-1">
        {['all', 'approved', 'pending'].map(f => (
          <button
            key={f}
            onClick={() => setFilter(f)}
            className={`text-[10px] font-mono px-2 py-1 rounded transition-colors capitalize
              ${filter === f ? 'bg-blue-500/10 text-blue-400 border border-blue-500/20' : 'text-slate-500 hover:text-slate-300'}`}
          >
            {f} ({f === 'all' ? assetRels.length :
              f === 'approved' ? assetRels.filter(r => r.status === 'approved').length :
              assetRels.filter(r => r.status === 'suggested' || r.status === 'pending_review').length})
          </button>
        ))}
      </div>

      {/* Grouped relationships */}
      {Object.entries(grouped).map(([type, rels]) => {
        const rt = RELATIONSHIP_TYPES.find(r => r.key === type) || { label: type, icon: '↔', color: 'text-slate-400' };
        return (
          <div key={type}>
            <p className={`text-[10px] font-mono uppercase tracking-wider ${rt.color} mb-1.5`}>
              {rt.icon} {rt.label} ({rels.length})
            </p>
            <div className="space-y-1.5">
              {rels.map((r, i) => (
                <RelationshipCard
                  key={r.id || `${r.target_id}-${i}`}
                  rel={r}
                  onApprove={onApprove}
                  onReject={onReject}
                />
              ))}
            </div>
          </div>
        );
      })}

      {filtered.length === 0 && (
        <div className="text-center py-8">
          <Network className="w-8 h-8 text-slate-700 mx-auto mb-2" />
          <p className="text-xs text-slate-500">
            {filter === 'all' ? 'No connections yet' :
              filter === 'approved' ? 'No approved connections' : 'No pending suggestions'}
          </p>
        </div>
      )}
    </div>
  );
}

