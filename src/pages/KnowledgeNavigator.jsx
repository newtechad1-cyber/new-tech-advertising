function KnowledgeSearch({ allAssets, onSelectAsset, relationships }) {
  const [query, setQuery] = useState('');
  const [results, setResults] = useState([]);

  useEffect(() => {
    if (!query.trim()) { setResults([]); return; }
    const q = query.toLowerCase();
    
    // Search by title, type, domain, and tags — plus through relationships
    const directMatches = allAssets.filter(a =>
      a.title.toLowerCase().includes(q) ||
      a.type.toLowerCase().includes(q) ||
      (a.domain || '').toLowerCase().includes(q) ||
      (a.tags || '').toLowerCase().includes(q)
    );

    // Find assets connected to direct matches via relationships
    const directIds = new Set(directMatches.map(a => a.id));
    const connectedIds = new Set();
    relationships.forEach(r => {
      if (directIds.has(r.source_id) && r.status === 'approved') connectedIds.add(r.target_id);
      if (directIds.has(r.target_id) && r.status === 'approved') connectedIds.add(r.source_id);
    });

    const connectedMatches = allAssets.filter(a => connectedIds.has(a.id) && !directIds.has(a.id));

    setResults([
      ...directMatches.map(a => ({ ...a, matchType: 'direct' })),
      ...connectedMatches.map(a => ({ ...a, matchType: 'connected' })),
    ].slice(0, 30));
  }, [query, allAssets, relationships]);

  return (
    <div className="relative">
      <div className="relative">
        <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-slate-500" />
        <input
          type="text"
          value={query}
          onChange={e => setQuery(e.target.value)}
          placeholder='Search all knowledge... try "trust", "HVAC", or "authority"'
          className="w-full bg-slate-800/60 border border-slate-700/50 rounded-xl pl-9 pr-4 py-2.5 text-sm text-slate-200 placeholder:text-slate-500 focus:outline-none focus:border-blue-500/50 focus:ring-1 focus:ring-blue-500/20 transition-all"
        />
      </div>
      {results.length > 0 && (
        <div className="absolute top-full mt-1 left-0 right-0 bg-slate-900 border border-slate-700/50 rounded-xl shadow-2xl z-50 max-h-80 overflow-y-auto">
          {results.map((r, i) => {
            const meta = ASSET_TYPE_META[r.type] || { icon: Database, label: r.type };
            const Icon = meta.icon;
            return (
              <button
                key={`${r.type}-${r.id}-${i}`}
                onClick={() => { onSelectAsset(r); setQuery(''); }}
                className="w-full flex items-center gap-3 px-4 py-2.5 text-left hover:bg-slate-800/60 transition-colors border-b border-slate-800/50 last:border-0"
              >
                <Icon className="w-4 h-4 text-slate-400 flex-shrink-0" />
                <div className="flex-1 min-w-0">
                  <p className="text-sm text-slate-200 truncate">{r.title}</p>
                  <p className="text-[10px] text-slate-500">{meta.label} • {KNOWLEDGE_DOMAINS.find(d => d.key === r.domain)?.label || r.domain}</p>
                </div>
                {r.matchType === 'connected' && (
                  <span className="text-[9px] font-mono text-cyan-400 bg-cyan-500/10 px-1.5 py-0.5 rounded">VIA RELATIONSHIP</span>
                )}
              </button>
            );
          })}
        </div>
      )}
    </div>
  );
}

