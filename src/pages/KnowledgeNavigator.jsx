function DomainItem({ domain, assets, isExpanded, onToggle, onSelectAsset, selectedAssetId }) {
  const Icon = domain.icon;
  const domainAssets = assets.filter(a => a.domain === domain.key);
  const count = domainAssets.length;

  // Group by type
  const groups = {};
  domainAssets.forEach(a => {
    const label = ASSET_TYPE_META[a.type]?.label || a.type;
    if (!groups[label]) groups[label] = [];
    groups[label].push(a);
  });

  return (
    <div className="mb-1">
      <button
        onClick={onToggle}
        className={`w-full flex items-center gap-2 px-3 py-2 rounded-lg text-left hover:bg-slate-800/60 transition-all ${isExpanded ? 'bg-slate-800/40' : ''}`}
      >
        <Icon className={`w-4 h-4 ${domain.color} flex-shrink-0`} />
        <span className="text-sm text-slate-200 flex-1 truncate">{domain.label}</span>
        <span className={`text-[10px] font-mono px-1.5 py-0.5 rounded ${domain.bg} ${domain.color}`}>{count}</span>
        <ChevronRight className={`w-3.5 h-3.5 text-slate-500 transition-transform ${isExpanded ? 'rotate-90' : ''}`} />
      </button>
      {isExpanded && (
        <div className="ml-4 pl-2 border-l border-slate-800 mt-1 space-y-0.5">
          {Object.entries(groups).map(([groupLabel, items]) => (
            <div key={groupLabel}>
              <p className="text-[10px] font-mono text-slate-500 uppercase tracking-wider px-2 py-1">{groupLabel} ({items.length})</p>
              {items.map(asset => (
                <button
                  key={`${asset.type}-${asset.id}`}
                  onClick={() => onSelectAsset(asset)}
                  className={`w-full flex items-center gap-2 px-2 py-1.5 rounded text-left text-xs transition-all
                    ${selectedAssetId === asset.id ? 'bg-blue-500/10 text-blue-300 border border-blue-500/20' : 'text-slate-400 hover:text-slate-200 hover:bg-slate-800/40'}`}
                >
                  <AssetTypeIcon type={asset.type} className="w-3 h-3 flex-shrink-0" />
                  <span className="truncate">{asset.title}</span>
                </button>
              ))}
            </div>
          ))}
          {count === 0 && <p className="text-[10px] text-slate-600 px-2 py-1 italic">No assets yet</p>}
        </div>
      )}
    </div>
  );
}

