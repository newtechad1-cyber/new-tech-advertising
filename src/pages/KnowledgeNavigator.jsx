function ConfidenceBadge({ score }) {
  const color = score >= 80 ? 'text-emerald-400 bg-emerald-500/10' :
    score >= 60 ? 'text-blue-400 bg-blue-500/10' :
    score >= 40 ? 'text-amber-400 bg-amber-500/10' :
    'text-slate-400 bg-slate-500/10';
  return (
    <span className={`text-[10px] font-mono px-1.5 py-0.5 rounded ${color}`}>
      {score}%
    </span>
  );
}

function StatusBadge({ status }) {
  const config = {
    suggested: { color: 'text-blue-400 bg-blue-500/10', label: 'Suggested' },
    pending_review: { color: 'text-amber-400 bg-amber-500/10', label: 'Pending' },
    approved: { color: 'text-emerald-400 bg-emerald-500/10', label: 'Approved' },
    rejected: { color: 'text-red-400 bg-red-500/10', label: 'Rejected' },
    archived: { color: 'text-slate-400 bg-slate-500/10', label: 'Archived' },
  };
  const c = config[status] || config.suggested;
  return <span className={`text-[10px] font-mono px-1.5 py-0.5 rounded ${c.color}`}>{c.label}</span>;
}

function DomainBadge({ domainKey }) {
  const domain = KNOWLEDGE_DOMAINS.find(d => d.key === domainKey);
  if (!domain) return null;
  const Icon = domain.icon;
  return (
    <span className={`inline-flex items-center gap-1 text-[10px] font-mono px-1.5 py-0.5 rounded ${domain.bg} ${domain.color} border ${domain.border}`}>
      <Icon className="w-3 h-3" />
      {domain.label}
    </span>
  );
}

function AssetTypeIcon({ type, className = 'w-4 h-4' }) {
  const meta = ASSET_TYPE_META[type];
  if (!meta) return <Database className={className + ' text-slate-500'} />;
  const Icon = meta.icon;
  return <Icon className={className} />;
}

